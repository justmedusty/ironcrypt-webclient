    <h1>About this app</h1>

    <p>
        This is an PGP centric file storage web app created with ktor and svelte. I created this after my previous encrypted messaging service Encryptix, which utilized ktor as
        well for the backend and react for the front end. You can create your own svelte app with the following terminal command from a project directory.
    </p>

    <pre>npm create svelte@latest</pre>

    <p>
        The way this service works is my servers host files and messages however they are automatically encrypted with your public key, meaning that once they are stored they cannot be decrypted
        except by the person they were intended for, allowing for a very strong sense (and reality) of privacy. I enjoy building privacy centric applications and will continue to do so in my quest to learn and build things.
    </p>
